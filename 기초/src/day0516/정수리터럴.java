package day0516;

public class 정수리터럴 {

	public static void main(String[] args) {
		
		// 2진수 : 0b , 8진수 : 0 , 16진수 : 0x
		int var1 = 0b1011;
		int var2 = 0206;
		int var3 = 1234;
		int var4 = 0xb3;
		
		System.out.println("var1 : " + var1);
		System.out.println("var2 : " + var2);
		System.out.println("var3 : " + var3);
		System.out.println("var4 : " + var4);

		byte b1 = 127;
		
		System.out.println("b1 : " + b1);
		
	}

}
